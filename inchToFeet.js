function calcius(c){
    var f = (9*c)/5 +32;
    return f;
}


var fahrenhight = calcius(0);
console.log(fahrenhight);

var fahrenhight = calcius(40);
console.log(fahrenhight);

var fahrenhight = calcius(60);
console.log(fahrenhight);

var fahrenhight = calcius(100);
